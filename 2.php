								<center><b><h3>PANTAI KUTA</h3></b></center>
					<p><dd>Pantai Kuta adalah sebuah tempat pariwisata yang terletak kecamatan Kuta, sebelah selatan Kota Denpasar, Bali,
                       Indonesia. Daerah ini merupakan sebuah tujuan wisata turis mancanegara dan telah menjadi objek wisata andalan Pulau
                       Bali sejak awal tahun 1970-an. Pantai Kuta sering pula disebut sebagai pantai matahari terbenam (sunset beach)
                       sebagai lawan dari pantai Sanur. Selain itu, Lapangan Udara I Gusti Ngurah Rai terletak tidak jauh dari Kuta.</p>
					<p><dd>Sebelum menjadi objek wisata, Kuta merupakan sebuah pelabuhan dagang tempat produk lokal diperdagangkan kepada 
                       pembeli dari luar Bali. Pada abad ke-19, Mads Lange, seorang pedagang Denmark, datang ke Bali dan mendirikan basis
                       perdagangan di Kuta. Ia ahli bernegosiasi sehingga dirinya terkenal di antara raja-raja Bali dan Belanda.</p>
					<p><dd>Pantai Kuta terkenal memiliki ombak yang bagus untuk olahraga selancar (surfing)[2], terutama bagi peselancar
                       pemula. Selain keindahan pantai, wisata pantai Kuta juga menawarkan berbagai jenis hiburan seperti bar, restoran,
                       pertokoan, restoran, hotel, dan toko-toko kelontong, serta pedagang kaki lima di sepanjang pantai menuju pantai
                       Legian.</p>
                    <p><dd>Pantai Kuta dapat ditempuh dengan waktu sekitar 10 menit dari Bandara Internasional Ngurah Rai dalam kondisi 
                       jalanan lancar.Sebagai tempat wisata pantai, pantai Kuta dilengkapi lahan parkir di sepanjang pantai, kamar mandi 
                       umum, payung pantai, kios makanan dan minuman, serta tempat penyewaan papan selancar.Setiap tahun, pengunjung pantai 
                       Kuta kerap mengeluhkan masalah kebersihan dan tumpukan sampah di pantai Kuta, terutama saat musim liburan. Hal 
                       tersebut mempengaruhi penilaian wisatawan domestik maupun manca negara terhadap citra pantai Kuta.Selain disebabkan
                       aktivitas pengunjung dan penjual di sepanjang pantai Kuta, sampah-sampah di pantai Kuta juga diakibatkan hembusan
                       angin barat setiap tahunnya yang membawa sampah dari muara-muara sungai terdekat ke pantai.Permasalahan ini berusaha
                       diatasi oleh prajuru Desa Adat Kuta dan anggota Badan Penyelamat Wisata Tirta (Balawista) yang merupakan mitra dari
                       Dinas Kebersihan dan Pertamanan Kabupaten Badung. Setiap pagi, Dinas Kebersihan dan Pertamanan juga aktif mengoperasi
                       kan mobil loader untuk memunguti sampah di pagi hari.[6][7] Permasalahan ini juga memperoleh perhatian utama dari TNI
                       , berbagai organisasi masyarakat, dan industri-industri pariwisata yang berada di wilayah Pantai Kuta.</p>
